package com.ryan.ctf.math;

/*
 * A two dimensional rectangle using integer coordinates
 */
public class Rectanglef {

  public float X1;
  public float Y1;
  public float X2;
  public float Y2;

  public Rectanglef() { }

  public Rectanglef(float x1, float y1, float x2, float y2) {
    this.X1 = x1;
    this.Y1 = y1;
    this.X2 = x2;
    this.Y2 = y2;
  }
}
